def test_trapezoidal_integral():
    """Test the trapezoidal_integral helper function."""
    y = [10, 20, 30] # kW
    x = [datetime(2023, 1, 1, 0, 0, 0), datetime(2023, 1, 1, 1, 0, 0), datetime(2023, 1, 1, 2, 0, 0)] # hourly
    # (10+20)/2 * 1 + (20+30)/2 * 1 = 15 * 1 + 25 * 1 = 40 kWh
    assert trapezoidal_integral(y, x) == pytest.approx(40.0)

    # Test with non-uniform time steps
    y_non_uniform = [10, 20, 30]
    x_non_uniform = [datetime(2023, 1, 1, 0, 0, 0), datetime(2023, 1, 1, 0, 30, 0), datetime(2023, 1, 1, 2, 0, 0)]
    # (10+20)/2 * 0.5 + (20+30)/2 * 1.5 = 15 * 0.5 + 25 * 1.5 = 7.5 + 37.5 = 45 kWh
    assert trapezoidal_integral(y_non_uniform, x_non_uniform) == pytest.approx(45.0)

    # Test with single point (should return 0)
    assert trapezoidal_integral([10], [datetime(2023, 1, 1)]) == 0.0

    # Test with empty lists (should return 0)
    assert trapezoidal_integral([], []) == 0.0